package br.com.abc.introducao.operadores;

public class Operadores {
    // +, -, *, /, %
    public static void main (String[] args){
        int numero1 = 10;
        int numero2 = 20;
        int soma = numero1 + numero2;
        System.out.println("A soma e: " + soma);
        System.out.println("A soma e: " + (numero1 + numero2));
        System.out.println("A soma e: " + numero1 + numero2);
    }
}
